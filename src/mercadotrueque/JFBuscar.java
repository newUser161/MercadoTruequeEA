    /**
     * Accion click en buscar, obtiene os criterios de busqueda y genera
     * SolicitudBusqueda
     *
     * @since mercadotrueque 0.1
     */
    private void botonBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonBuscarMouseClicked
        // comprobar seleccion de region
        if (regionSeleccion == -1) {
            JOptionPane.showMessageDialog(null, "Region no Seleccionada");
        }
        // obtenemos categoria
        if (categoriaSeleccion == null) {
            JOptionPane.showMessageDialog(null, "Categoria no Seleccionada");
        }
        //verificamos si se seleccionaron
        if (categoriaSeleccion != null && regionSeleccion != -1) {
            //obtenemos la opcion de orden de busqueda
            int opcionBusqueda;
            if (opcionSinOrden.isSelected()) {
                opcionBusqueda = SIN_ORDEN;
            } else if (opcionAscendente.isSelected()) {
                opcionBusqueda = ASCENDENTE;
            } else {
                opcionBusqueda = DESCENDENTE;
            }
            // crear solicitud //
            SolicitudBusqueda solicitudNueva = new SolicitudBusqueda(regionSeleccion, categoriaSeleccion, opcionBusqueda);
            // asignar a atributo//
            solicitudBusqueda = solicitudNueva;
            salir = true;
        }
    }//GEN-LAST:event_botonBuscarMouseClicked
