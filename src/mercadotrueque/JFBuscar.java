    private void refrescarColorRegiones() {
        for(int i=0;i<panelRegiones.getComponentCount();i++){
            panelRegiones.getComponent(i).setBackground(Color.white);
        }
    }

    private void refrescarCategoria() {
        for(int i=0;i<panelCategoria.getComponentCount();i++){
            panelCategoria.getComponent(i).setBackground(new Color(204,250,146));
        }
    }
