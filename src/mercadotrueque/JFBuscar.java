   /**
     * Inicializa posicion y visual de ventana, ademas inicia valores de los
     * atributos
     *
     * @since mercadotrueque 0.2
     */
    public void start() {
        if (!this.isVisible()) {
            this.setLocationRelativeTo(null);
        }
        this.setVisible(true);
        salir = false;
        textoRegion.setText(" - Ninguna seleccionada - ");
        textoCategoria.setText(" - Ninguna seleccionada - ");
        categoriaSeleccion = null;
        regionSeleccion = -1;
        solicitudBusqueda = null;
        opcionSinOrden.setSelected(true);
        refrescarCategoria();
        refrescarColorRegiones();
    }

